function handleDownload() {
    // 需要导出的数据
    const exportArr = [
        // { key: 1, name: "zhangsan" },
        // { key: 2, name: "lisi" },
    ]

    for (const Message in Net_Message) {
        if (Message == "id_game_name") continue;
        exportArr.push({
            key: Message,
            chinese_zh: Net_Message[Message]['chinese_zh'],
            english: Net_Message[Message]["english"],
            Dai: Net_Message[Message]["Dai"],

            Vietnamese: Net_Message[Message]["Vietnamese"],
            Portugal: Net_Message[Message]["Portugal"],
            Indonesia: Net_Message[Message]["Indonesia"],
            Malaysia: Net_Message[Message]["Malaysia"],
            Spanish: Net_Message[Message]["Spanish"],
            Hindi: Net_Message[Message]["Hindi"],
        });
    }
    for (const Message in Net_Message["id_game_name"]) {
        exportArr.push({
            key: `id_game_name-${Message}`,
            chinese_zh: Net_Message["id_game_name"][Message]['chinese_zh'],
            english: Net_Message["id_game_name"][Message]["english"],
            Dai: Net_Message["id_game_name"][Message]["Dai"],

            Vietnamese: Net_Message["id_game_name"][Message]["Vietnamese"],
            Portugal: Net_Message["id_game_name"][Message]["Portugal"],
            Indonesia: Net_Message["id_game_name"][Message]["Indonesia"],
            Malaysia: Net_Message["id_game_name"][Message]["Malaysia"],
            Spanish: Net_Message["id_game_name"][Message]["Spanish"],
        });
    }


    // 自定义下载的header，注意是数组中的数组哦
    const Header = [['id', '中文', '英语', "泰语", "越南语", "葡萄牙语", "印尼", "马来西亚", "西班牙语", "印地语"]];

    // 官方文档中的描述：converts an array of arrays of JS data to a worksheet.
    const headerWs = XLSX.utils.aoa_to_sheet(Header);
    const ws = XLSX.utils.sheet_add_json(headerWs, exportArr, { skipHeader: true, origin: "A2" });

    /* 新建空workbook，然后加入worksheet */
    const wb = XLSX.utils.book_new();

    // 可以自定义下载之后的sheetname
    XLSX.utils.book_append_sheet(wb, ws, "sheetName");

    /* 生成xlsx文件 */
    XLSX.writeFile(wb, "服务器需要的翻译.xlsx");
    console.warn("完毕");
}


